package basics;

public class RelationalOperators {

	public static void main(String args[])
	{
	int num1= 20;
	int num2= 20;
	System.out.println(num1>num2);
	System.out.println(num1<num2);
	System.out.println(num1>=num2);
	System.out.println(num1<=num2);
	System.out.println(num1==num2);
	System.out.println(num1!=num2);
	}
}
